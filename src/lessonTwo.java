public class lessonTwo {
    public static void main(String[] args) {
//        Declaring Variables
        int num1;
        float num2;
        double num3;
        char letter;
        String full_names;
        // Initialising Variables
        num1 = 23;
        num2 = 12.345f;
        num3 = 122323.6789;
        letter = 'A';
        full_names = "Alton Wambwa";
        // Results
        System.out.println("Integer Value:" + num1);
        System.out.println("Decimal Value:" + num2);
        System.out.println("Double Value:" + num3);
        System.out.println("First Letter:" + letter);
        System.out.println("Full Names :" + full_names);
    }
}
